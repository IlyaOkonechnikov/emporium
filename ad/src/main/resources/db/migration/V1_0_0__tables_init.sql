create table category
(
    id        serial  not null
        constraint category_pkey
            primary key,
    name      varchar not null,
    parent_id integer default 0
);

create unique index category_id_uindex
    on category (id);

create table field
(
    id        serial  not null
        constraint field_pkey
            primary key,
    name      varchar not null,
    numerical boolean default false
);

create unique index field_id_uindex
    on field (id);

create table category_field
(
    id          serial not null
        constraint category_field_pkey
            primary key,
    category_id integer default 0
        constraint fk_category_id
            references category
            on update cascade on delete cascade,
    field_id    integer default 0
        constraint fk_field_id
            references field
            on update cascade on delete cascade
);

create unique index category_field_id_uindex
    on category_field (id);

create table ad
(
    id          bigserial        not null
        constraint ad_pkey
            primary key,
    account_id  bigserial        not null,
    category_id integer          not null,
    description varchar(1500)    not null,
    price       double precision not null,
    active      boolean   default true,
    create_date timestamp default now(),
    update_date timestamp default now()
);

create unique index ad_uindex
    on ad (id);

create table ad_field
(
    id              bigserial not null
        constraint ad_field_pkey
            primary key,
    ad_id           integer   not null,
    name            varchar   not null,
    text_value      varchar,
    numerical_value double precision
);

create unique index ad_field_uindex
    on ad_field (id);

